var b = 1
while( b <= 5 ) { //enquanto b for menor ou igual a 5
      console.log(`Laço ${b} com WHILE`)// faça isso 
      b++//encrementando +1 a cada volta
}
console.log("----------------------")

//     inicio ;condição;encremento
for( var c = 1 ; c <= 5 ; c++ ){//c=1 se c for menor ou igual a 5 adicione +1 a cada volta
      console.log(`Laço ${c} com FOR`)//faça isso a cada volta
}
console.log("----------------------")
